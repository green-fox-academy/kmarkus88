public class NumberOperations {

    public static void main(String[] args) {
        // Arithmetric operations
        System.out.println(13 + 22);

        System.out.println(13 - 22);

        System.out.println(22 * 13);

        System.out.println(22. / 13.);

        System.out.println(22 / 13);

        System.out.println(22 % 13);
    }
}

